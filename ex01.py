#entrada de dados
valor1 = float(input("informe o valor 1: ")) # recebe o valor 1
valor2 = float(input("informe o valor 2: "))
valor3 = float(input("informe o valor 3: "))

# saida de dados
print(f"entrada: {valor1}, {valor2}, {valor3}")  # imprime os valores de entrada
print(f"saida: {valor3}, {valor2}, {valor1}") # imprime os valores de saida


